function findColName(name){
	let text = name.toUpperCase()
  let total = 0
  let count = name.length
	for(let i in text){
  	const char = text.charCodeAt(i) - 64
    count--;
    total += Math.pow(26,count) * char
    if(count===0){
      return total
		}
  }
}

